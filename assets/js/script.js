// --- Left sidebar toggle ---
const leftSidebar = document.querySelector('.leftSidebar');
const toggleBtn = document.querySelector('.burger-input');

if (toggleBtn && leftSidebar) {
    toggleBtn.addEventListener('click', (event) => {
        leftSidebar.classList.toggle('active');
        event.stopPropagation();
    });

    document.addEventListener('click', (event) => {
        if (!leftSidebar.contains(event.target) && !toggleBtn.contains(event.target)) {
            leftSidebar.classList.remove('active');
        }
    });
}

// --- Scroll-spy: highlight current section in BOTH the left sidebar and the
//     bottom nav bar (the bottom bar is generated by main.js on smaller screens). ---
(function () {
    function initSpy() {
        var links = Array.prototype.slice.call(document.querySelectorAll(
            '.leftSidebar .list .list-item a[href], ' +
            '.leftSidebar .list .list-item a[data-section], ' +
            '.navigation ul li a[href]'
        ));
        if (!links.length) return;

        function resolveSection(link) {
            var candidates = [];
            var href = link.getAttribute('href');
            if (href && href.charAt(0) === '#' && href.length > 1) candidates.push(href.slice(1));
            var ds = link.getAttribute('data-section');
            if (ds) {
                candidates.push(ds);
                if (ds.slice(-6) === 'Tittle') candidates.push(ds.slice(0, -6));
            }
            for (var i = 0; i < candidates.length; i++) {
                var el = document.getElementById(candidates[i]);
                if (!el) continue;
                if (el.tagName === 'SECTION') return el;
                var sec = el.closest && el.closest('section');
                if (sec) return sec;
                // e.g. "#page-wrapper" -> first <section> inside it (the hero)
                var inner = el.querySelector && el.querySelector('section');
                if (inner) return inner;
            }
            return null;
        }

        // Group all links (both navs) by the section they point to
        var groups = [];
        function groupFor(section) {
            for (var i = 0; i < groups.length; i++) if (groups[i].section === section) return groups[i];
            var g = { section: section, links: [] };
            groups.push(g);
            return g;
        }
        links.forEach(function (link) {
            var section = resolveSection(link);
            if (section) groupFor(section).links.push(link);
        });
        if (!groups.length) return;

        function setActive(section) {
            links.forEach(function (l) { l.classList.remove('active'); });
            for (var i = 0; i < groups.length; i++) {
                if (groups[i].section === section) {
                    groups[i].links.forEach(function (l) { l.classList.add('active'); });
                    break;
                }
            }
        }

        if ('IntersectionObserver' in window) {
            var io = new IntersectionObserver(function (entries) {
                entries.forEach(function (entry) {
                    if (entry.isIntersecting) setActive(entry.target);
                });
            }, { rootMargin: '-45% 0px -45% 0px', threshold: 0 });
            groups.forEach(function (g) { io.observe(g.section); });
        } else {
            var onScroll = function () {
                var mid = window.scrollY + window.innerHeight / 2;
                var current = null;
                groups.forEach(function (g) {
                    var top = g.section.offsetTop;
                    if (mid >= top && mid < top + g.section.offsetHeight) current = g.section;
                });
                if (current) setActive(current);
            };
            window.addEventListener('scroll', onScroll, { passive: true });
            onScroll();
        }
    }

    // main.js builds the bottom nav synchronously before this file, but guard anyway.
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initSpy);
    } else {
        initSpy();
    }
})();
